    private void btnAgregarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAgregarProductoActionPerformed
        Producto seleccionado = (Producto) cboProductos.getSelectedItem();
        if(seleccionado == null) return;

        int cantidad = (int) spnCantidadProductos.getValue();
        if(cantidad <= 0) {
            JOptionPane.showMessageDialog(this, "La cantidad debe ser mayor a 0");
            return;
        }

        float precio = seleccionado.getPrecio();
        float subtotal = precio * cantidad;

        modeloTabla.addRow(new Object[]{
            seleccionado.getNombre(),
            cantidad,
            String.format("S/. %.2f", precio),
            String.format("S/. %.2f", subtotal)
        });

        totalProductos += subtotal;
        txtPrecioTotal.setText(String.format("S/. %.2f", totalProductos));

        // Resetear spinner
        spnCantidadProductos.setValue(1);
    }//GEN-LAST:event_btnAgregarProductoActionPerformed

