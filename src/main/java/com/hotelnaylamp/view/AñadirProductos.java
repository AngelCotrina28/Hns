    private ProductoController productoController = new ProductoController();
    private DefaultTableModel modeloTabla;
    private int habitacionAsignada;
    private float totalProductos = 0;

    public AñadirProductos(Window parent, boolean modal, int habitacionAsignada) {
        super(parent, modal ? ModalityType.APPLICATION_MODAL : ModalityType.MODELESS);
        initComponents();
        this.habitacionAsignada = habitacionAsignada;
        setTitle("Añadir Productos - Hab: " + habitacionAsignada);
        setLocationRelativeTo(parent);
        inicializarTabla();
        cargarProductosEnCombo();
        configurarEventos();
    }   
    
    private void inicializarTabla() {
        modeloTabla = new DefaultTableModel( new String[]{"Producto", "Cantidad", "Precio", "SubTotal"} , 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false; // ninguna celda editable
            }
        };
        tblProductosSeleccionados.setModel(modeloTabla);
    }

    private void cargarProductosEnCombo() {
        List<Producto> productos = productoController.cargarProductos();
        cboProductos.removeAllItems();
        for(Producto p : productos) {
            cboProductos.addItem(p);
        }
        // Mostrar precio del primero seleccionado
        if(!productos.isEmpty()) {
            txtPrecio.setText(
                String.valueOf(productos.get(0).getPrecio())
            );
        }
    }
    
    private void configurarEventos() {
        // Actualizar precio cuando cambia el producto
        cboProductos.addActionListener(e -> {
            Producto productoSeleccionado = (Producto) cboProductos.getSelectedItem();
            if(productoSeleccionado != null) {
                txtPrecio.setText(
                    String.valueOf(productoSeleccionado.getPrecio())
                );
            }
        });
    }
    
    public float getTotalProductos() {
        return totalProductos;
    }

    public List<Object[]> getProductosAgregados() {
        List<Object[]> productos = new ArrayList<>();
        for(int i = 0; i < modeloTabla.getRowCount(); i++) {
            productos.add(new Object[]{
                modeloTabla.getValueAt(i, 0), // nombre
                modeloTabla.getValueAt(i, 1), // cantidad
                modeloTabla.getValueAt(i, 2)  // precio
            });
        }
        return productos;
    }
