    private void btnEliminarProductoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarProductoActionPerformed
        int filaSeleccionada = tblProductosSeleccionados.getSelectedRow();
        if(filaSeleccionada == -1) {
            JOptionPane.showMessageDialog(this, "Selecciona un producto para quitar");
            return;
        }

        // Restamos el subtotal del total
        String subtotalStr = modeloTabla.getValueAt(filaSeleccionada, 3)
                                        .toString().replace("S/. ", "");
        totalProductos -= Float.parseFloat(subtotalStr);
        txtPrecioTotal.setText(String.format("S/. %.2f", totalProductos));

        modeloTabla.removeRow(filaSeleccionada);
    }//GEN-LAST:event_btnEliminarProductoActionPerformed

    
